/*******************************************************************************
 * Animal()
 * Default constructor
*******************************************************************************/
Animal::Animal() {
	age = 1;
	cost = 0;
	babies = 0;
	foodCost = 10;
	payoff = 0;
	type = "Animal";
}

/*******************************************************************************
 * ~Animal()
 * Default destructor
*******************************************************************************/
Animal::~Animal()
{}

/*******************************************************************************
 * Animal(int)
 * Animal constructor that allows specific age.
*******************************************************************************/
Animal::Animal(int a) {
	age = a;
}

/*******************************************************************************
 * getAge()
 * returns Animal age
*******************************************************************************/
int Animal::getAge() {
	return age;
}

/*******************************************************************************
 * getCost()
 * returns Animal cost
*******************************************************************************/
int Animal::getCost() {
	return cost;
}

/*******************************************************************************
 * getBabies()
 * returns Animal babies
*******************************************************************************/
int Animal::getBabies() {
	return babies;
}

/*******************************************************************************
 * getFoodCost()
 * returns Animal foodCost
*******************************************************************************/
int Animal::getFoodCost() {
	return foodCost;
}

/*******************************************************************************
 * getPayoff()
 * return Animal payoff
*******************************************************************************/
int Animal::getPayoff() {
	return payoff;
}

/*******************************************************************************
 * getType()
 * set Animal age
*******************************************************************************/
string Animal::getType() {
	return type;
}

/*******************************************************************************
 * increaseAge()
 * Description: Increments animal age
*******************************************************************************/
void Animal::increaseAge() {
	age++;
}
